// Generate 30 days of daily volume
const _generateDailyVolume = () => {
  const rows = []
  // Work week pattern: Mon–Fri higher, Sat–Sun lower
  // Trend: slight upward over 30 days
  const weekdayBase = { handoffs: 82, continuations: 44, resolved: 108, escalated: 7, sensitive: 4 }
  const weekendBase = { handoffs: 24, continuations: 11, resolved: 29, escalated: 2, sensitive: 1 }
  for (let i = 29; i >= 0; i--) {
    const d = new Date('2026-05-28T00:00:00Z')
    d.setUTCDate(d.getUTCDate() - i)
    const dow = d.getUTCDay() // 0=Sun, 6=Sat
    const isWeekend = dow === 0 || dow === 6
    const base = isWeekend ? weekendBase : weekdayBase
    const growth = (29 - i) / 29 * 0.18  // 18% growth over 30 days
    const jitter = () => Math.round((Math.random() - 0.5) * 12)
    rows.push({
      date: d.toISOString().slice(0, 10),
      handoffs: Math.max(0, Math.round(base.handoffs * (1 + growth)) + jitter()),
      continuations: Math.max(0, Math.round(base.continuations * (1 + growth)) + jitter()),
      resolved: Math.max(0, Math.round(base.resolved * (1 + growth)) + jitter()),
      escalated: Math.max(0, Math.round(base.escalated * (1 + growth)) + Math.round(jitter() / 4)),
      sensitive: Math.max(0, Math.round(base.sensitive * (1 + growth)) + Math.round(jitter() / 6)),
    })
  }
  return rows
}

